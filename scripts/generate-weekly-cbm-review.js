#!/usr/bin/env node

/**
 * Generate Weekly CBM Review draft from durable Cockpit data.
 *
 * Source:
 *   docs/cbm-capability-cockpit-data.js
 * Outputs:
 *   docs/weekly-cbm-review-latest.md
 *   brain/evolution-notes/<YYYY-MM-DD>-cbm-pmo-review.md
 *
 * This script intentionally does not update CONSTITUTION.md and does not
 * promote any Cell to validated / operating / compounding.
 */

const fs = require('fs');
const path = require('path');
const vm = require('vm');

const root = process.cwd();
const sourcePath = path.join(root, 'docs/cbm-capability-cockpit-data.js');
const latestPath = path.join(root, 'docs/weekly-cbm-review-latest.md');
const date = new Date().toISOString().slice(0, 10);
const evolutionPath = path.join(root, `brain/evolution-notes/${date}-cbm-pmo-review.md`);

function loadCockpitData() {
  const raw = fs.readFileSync(sourcePath, 'utf8');
  const sandbox = { window: {} };
  vm.createContext(sandbox);
  vm.runInContext(raw, sandbox, { filename: sourcePath });
  const data = sandbox.window.YUANLI_CBM_CAPABILITY_COCKPIT;
  if (!data) throw new Error('Missing window.YUANLI_CBM_CAPABILITY_COCKPIT');
  return data;
}

function table(rows) {
  return [
    '| CBM Cell | Status | Evidence | Gap | Decision |',
    '|---|---|---:|---:|---|',
    ...rows.map(c => `| \`${c.cbm_cell_id}\` | ${c.status} | ${c.evidence_strength} | ${c.gap_score} | ${c.is_validated ? 'validated' : 'keep draft'} |`),
  ].join('\n');
}

function renderReview(data) {
  const cells = data.pilot_cells || [];
  const h = data.system_health || {};
  const next = data.next_p0 || {};
  return `# Weekly CBM Review · ${date}

> Generated by: \`scripts/generate-weekly-cbm-review.js\`  
> Source: \`docs/cbm-capability-cockpit-data.js\`

---

## 1. System Health

\`\`\`text
Stage: ${data.stage || '-'}
Overall maturity: ${(data.maturity && data.maturity.overall) || '-'}
Total CBM cells: ${h.total_cells ?? '-'}
Draft cells: ${h.draft_cells ?? '-'}
Validated cells: ${h.validated_cells ?? '-'}
Backwrite coverage: ${h.backwrite_coverage || '-'}
False-green risks: ${h.false_green_risks ?? '-'}
\`\`\`

---

## 2. Capability Changes

${table(cells)}

---

## 3. Evidence Strength

Current medium-evidence cells:

${cells.filter(c => c.evidence_strength === 'medium').map(c => `- \`${c.cbm_cell_id}\``).join('\n') || '- none'}

No pilot cell should be marked validated unless there is real external or repeated operating evidence.

---

## 4. False Green Risk

Current false-green risk count:

\`\`\`text
${h.false_green_risks ?? 0}
\`\`\`

Hard rules:

${(data.hard_rules || []).map(r => `- ${r}`).join('\n')}

---

## 5. Human Decisions Needed

${cells.filter(c => c.is_human_decision_needed).map(c => `- \`${c.cbm_cell_id}\`: ${c.next_decision || '-'}; gates: ${(c.approval_gate || []).join(' / ')}`).join('\n') || '- none'}

---

## 6. Next P0 Capability

\`\`\`text
${next.task || next.id || '-'}
\`\`\`

Reason:

\`\`\`text
${next.reason || '-'}
\`\`\`

---

## 7. Do Not Upgrade Yet

${cells.map(c => `- Do not upgrade \`${c.cbm_cell_id}\` beyond ${c.status} / ${c.evidence_strength}.`).join('\n')}
`;
}

function renderEvolution(data) {
  const next = data.next_p0 || {};
  return `# Evolution Note · Weekly PMO → CBM Review

Date: ${date}

Source: \`docs/cbm-capability-cockpit-data.js\`

---

## What Changed

Weekly CBM Review was generated from durable Cockpit data.

## Status Decision

No pilot cell is upgraded to validated.

## Next P0 Capability

${next.task || next.id || '-'}

## Canonical Boundary

This note is a governance artifact. It does not update \`CONSTITUTION.md\`.
`;
}

function main() {
  const data = loadCockpitData();
  fs.writeFileSync(latestPath, renderReview(data));
  fs.mkdirSync(path.dirname(evolutionPath), { recursive: true });
  fs.writeFileSync(evolutionPath, renderEvolution(data));
  console.log(`Weekly CBM review written to ${latestPath}`);
  console.log(`Evolution note written to ${evolutionPath}`);
}

main();
