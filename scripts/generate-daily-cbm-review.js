#!/usr/bin/env node

/**
 * Generate Daily CBM Iteration Review draft from durable Cockpit data.
 *
 * Source:
 *   docs/cbm-capability-cockpit-data.js
 * Optional source:
 *   docs/cbm-pmo-sync-latest.json
 * Outputs:
 *   docs/daily-cbm-review-latest.md
 *   brain/evolution-notes/<YYYY-MM-DD>-daily-cbm-review.md
 *
 * Boundary:
 *   This script does not update CONSTITUTION.md and does not promote any Cell
 *   to validated / operating / compounding.
 */

const fs = require('fs');
const path = require('path');
const vm = require('vm');

const root = process.cwd();
const sourcePath = path.join(root, 'docs/cbm-capability-cockpit-data.js');
const syncPath = path.join(root, 'docs/cbm-pmo-sync-latest.json');
const latestPath = path.join(root, 'docs/daily-cbm-review-latest.md');
const date = new Date().toISOString().slice(0, 10);
const evolutionPath = path.join(root, `brain/evolution-notes/${date}-daily-cbm-review.md`);

function loadCockpitData() {
  const raw = fs.readFileSync(sourcePath, 'utf8');
  const sandbox = { window: {} };
  vm.createContext(sandbox);
  vm.runInContext(raw, sandbox, { filename: sourcePath });
  const data = sandbox.window.YUANLI_CBM_CAPABILITY_COCKPIT;
  if (!data) throw new Error('Missing window.YUANLI_CBM_CAPABILITY_COCKPIT');
  return data;
}

function loadSyncReport() {
  if (!fs.existsSync(syncPath)) return null;
  return JSON.parse(fs.readFileSync(syncPath, 'utf8'));
}

function safeList(items, render) {
  if (!items || items.length === 0) return '- none';
  return items.map(render).join('\n');
}

function renderCellTable(cells) {
  return [
    '| CBM Cell | Status | Evidence | Gap | Next Decision | Human Gate |',
    '|---|---|---:|---:|---|---|',
    ...cells.map(c => `| \`${c.cbm_cell_id}\` | ${c.status || '-'} | ${c.evidence_strength || '-'} | ${c.gap_score ?? '-'} | ${c.next_decision || '-'} | ${(c.approval_gate || []).join(' / ') || '-'} |`),
  ].join('\n');
}

function renderReview(data, sync) {
  const cells = data.pilot_cells || data.cells || [];
  const h = data.system_health || {};
  const next = data.next_p0 || (sync && sync.next_p0) || {};
  const falseGreen = (sync && sync.false_green_risks) || h.false_green_risks || [];
  const humanGates = (sync && sync.human_gates) || [];
  const maturity = (data.maturity && data.maturity.overall) || (sync && sync.overall_maturity) || '-';

  return `# Daily CBM Iteration Review · ${date}

> Generated by: \`scripts/generate-daily-cbm-review.js\`  
> Source: \`docs/cbm-capability-cockpit-data.js\`  
> Mode: review draft only; no automatic maturity upgrade.

---

## 1. System Health

\`\`\`text
Stage: ${data.stage || (sync && sync.stage) || '-'}
Overall maturity: ${maturity}
Total CBM cells: ${h.total_cells ?? (sync && sync.cell_count) ?? '-'}
Pilot cells: ${h.pilot_cells ?? (sync && sync.pilot_cell_count) ?? '-'}
Validated cells: ${h.validated_cells ?? (sync && sync.validated_cells) ?? '-'}
False-green risks: ${Array.isArray(falseGreen) ? falseGreen.length : falseGreen}
\`\`\`

---

## 2. Capability Snapshot

${renderCellTable(cells)}

---

## 3. False Green Risk Check

${Array.isArray(falseGreen) ? safeList(falseGreen, r => `- ${typeof r === 'string' ? r : JSON.stringify(r)}`) : `- count: ${falseGreen}`}

Hard boundary:

\`\`\`text
No Evidence -> no green.
No Evidence Review -> no validated.
No Evolution Note -> no Done.
No Backwrite -> no compounding.
\`\`\`

---

## 4. Human Decisions Needed

${safeList(humanGates, g => `- ${g.cell || g.id || '-'}: ${g.decision || '-'}; gates: ${(g.gates || []).join(' / ') || '-'}`)}

---

## 5. External Evidence Watch

Current evidence priority:

\`\`\`text
#132 B2 External Evidence Coding from llm-wiki June 2026 transcripts
\`\`\`

Decision rule:

\`\`\`text
External source discovery does not upgrade maturity. Only coded user-language records, scorecard evidence, and human approval can upgrade B2 validation.
\`\`\`

---

## 6. Next Daily P0

\`\`\`text
${next.task || next.id || '#132 B2 External Evidence Coding'}
\`\`\`

Reason:

\`\`\`text
${next.reason || 'Convert existing external fieldwork evidence into coded B2 validation evidence before further productization.'}
\`\`\`

---

## 7. Do Not Upgrade Automatically

${cells.length ? cells.map(c => `- Do not upgrade \`${c.cbm_cell_id}\` beyond ${c.status || '-'} / ${c.evidence_strength || '-'}.`).join('\n') : '- No cells available in data source.'}
`;
}

function renderEvolution(data, sync) {
  const next = data.next_p0 || (sync && sync.next_p0) || {};
  return `# Evolution Note · Daily CBM Iteration Review

Date: ${date}

Source: \`docs/cbm-capability-cockpit-data.js\`

---

## What Changed

Daily CBM Iteration Review was generated as a review draft.

## Status Decision

No CBM cell is upgraded to validated / operating / compounding by this script.

## Next P0

${next.task || next.id || '#132 B2 External Evidence Coding'}

## Canonical Boundary

This note is a governance artifact. It does not update \`CONSTITUTION.md\`.
`;
}

function main() {
  const data = loadCockpitData();
  const sync = loadSyncReport();
  fs.writeFileSync(latestPath, renderReview(data, sync));
  fs.mkdirSync(path.dirname(evolutionPath), { recursive: true });
  fs.writeFileSync(evolutionPath, renderEvolution(data, sync));
  console.log(`Daily CBM review written to ${latestPath}`);
  console.log(`Evolution note written to ${evolutionPath}`);
}

main();
